    private void AbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirActionPerformed
        // TODO add your handling code here:
        
        JFileChooser kf = new JFileChooser();
        kf.showOpenDialog(null);
        
        File archivo= kf.getSelectedFile();
        
        try{
             
            FileReader filereader= new FileReader(archivo);
            BufferedReader br= new BufferedReader(filereader);
            String texto ="";
            String linea = "";
            while(((linea=br.readLine())!=null)){
                
                texto+= linea+"\n";
                
            }
            txtArea.setText(texto);
            JOptionPane.showMessageDialog(null, "Leyendo archivo correctamente");
        }catch (Exception e){

        }
        
        
 
    }//GEN-LAST:event_AbrirActionPerformed
 
    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // TODO add your handling code here:
        String NombreArchivo = txtNombreNewAchivo.getText();
        String carpeta = System.getProperty("user.dir");
        String direccionCompleta = (carpeta + "/" +NombreArchivo + ".txt");
        FileWriter ubicacion = null;
        
        try{
            ubicacion = new FileWriter(direccionCompleta);
             
        }catch(IOException ex){
            
//            Logger.getLogger(interfaz.class.getName()).log(Level.SEVERE,null,ex);
        }
        
        try{
            
            try (BufferedWriter escritor = new BufferedWriter(ubicacion)) {
                escritor.write(txtArea.getText());
            }
            
        }catch(Exception ex){
            
        }
            JOptionPane.showMessageDialog(null, "Archivo guardado correctamente");        
        
    }//GEN-LAST:event_GuardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if(java.awt.Desktop.isDesktopSupported()){
             
           java.awt.Desktop desktop= java.awt.Desktop.getDesktop();
           
           if(desktop.isSupported(java.awt.Desktop.Action.BROWSE)){
               try{
                   java.net.URI uri= new java.net.URI(miURL.getText());
                   desktop.browse(uri);
               }catch(URISyntaxException e){
                   
               } catch (IOException ex) {
                   java.util.logging.Logger.getLogger(interfaz.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
               }
           }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
 
